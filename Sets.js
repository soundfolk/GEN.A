function ques() {
    quesno++
    let i = Math.floor(Math.random() * data.length)
    next()
    questions.innerHTML = `<b>Q${quesno} :</b> ${data[i].question}`
    console.log(data[i].answer)
    data[i].option.forEach(answer => {
        let button = document.createElement("button")
        button.innerText = answer
        button.classList = "but"
        butcontainer.appendChild(button)
        button.onclick = (e) => {
            let select = e.target
            Array.from(butcontainer.children).forEach(button => {
                if (button.innerText == data[i].answer) {
                    button.classList.add("correct")
                }
                button.disabled = true
            })
            if (button.innerText == data[i].answer) {
                if (Score >= 40) {
                    is = 1
                }
                Score++
                total++
                setTimeout(() => {
                    ques()
                }, 1000)
                console.log(`Score ${Score}/${total}`)
            }
            else {
                select.classList.add("incorrect")
                setTimeout(() => {
                    ques()
                }, 1000)
                total++
                console.log(`Score ${Score}/${total}`)
            }
        }
    })
}

